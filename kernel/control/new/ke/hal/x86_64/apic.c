int has_apic (void)
{
   unsigned int eax=0;
   unsigned int ebx=0;
   unsigned int ecx=0;
   unsigned int edx=0;


// #bugbug
// Do we have cpuid support?

   cpuid( 1, eax, ebx, ecx, edx );

   return (int) (edx & CPUID_FEAT_EDX_APIC);
}


